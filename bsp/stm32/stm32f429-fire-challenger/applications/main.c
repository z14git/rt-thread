/* defined the LED0 pin: PH10 */
#define LED0_PIN GET_PIN(H, 10)

#define LCD_BL_GPIO_NUM GET_PIN(D, 7)
#define LCD_DISP_GPIO_NUM GET_PIN(D, 4)

DMA2D_HandleTypeDef hdma2d;

